    if pretrained:
        kwargs['init_weights'] = False
    model = VGG(make_layers(cfg['A'], AllPoints,batch_norm=True), **kwargs)
    if pretrained:
        model.load_state_dict(model_zoo.load_url(model_urls['vgg11_bn']))
    return model

